def build_map(size: int = 128) -> np.ndarray:
    """Map 10: A complex maze with multiple corridors, rooms, and obstacles.

    Layout:
    - 128x128 grid, boundary and internal structures are walls (0)
    - Purple traversable areas default to dirty (2)
    - Four charging stations at (37,42), (86,49), (32,79), (81,88)
    - Four NPCs at (20,10), (112,16), (62,113), (102,62)
    - Agent spawn at (80,16)
    """
    if size != 128:
        raise ValueError("Map 10 only supports size=128.")

    grid = np.zeros((size, size), dtype=np.int8)
    for i, row_str in enumerate(_GRID_DATA):
        for j, ch in enumerate(row_str):
            grid[i, j] = np.int8(ch)
    return grid


